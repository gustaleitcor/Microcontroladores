	ADDWF	R2	    ; ADIÇÃO AUX A R2
	MOVFW	X1
	ADDWF	R1	    ; ADIÇÃO X1 A R1
	BTFSC	STATUS	, C ; CHECAGEM DE OVERFLOW DA ADIÇÃO X1 A R1
	INCF	R2	, .1; SE OVERFLOW INCREMENTE R2 
	GOTO	LOOP_2	    ; C.C. GOTO LOOP_2
