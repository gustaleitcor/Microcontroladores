	BCF	STATUS	, C ; CLEAR CARRY BIT
	RLF	X1, .1	    ; ROTATE/SHIFT X1 FILE PARA ESQUERDA
	RLF	AUX, .1	    ; ROTATE/SHIFT AUX FILE PARA ESQUERDA
  	DECFSZ	COUNTER	    ; DECREMENTA O COUNTER
